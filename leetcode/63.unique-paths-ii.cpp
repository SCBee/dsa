class Solution
{
   public:
    // AC
    int uniquePathsWithObstacles(vector<vector<int>>& grid)
    {
        int m = grid.size();
        int n = grid[0].size();

        vector<vector<int>> res(m, vector<int>(n, 1));
        for (int i = m - 1; i >= 0; --i)
        {
            for (int j = n - 1; j >= 0; --j)
            {
                if (grid[i][j] == 1)  // obstacle cell
                {
                    res[i][j] = 0;
                }
                else if (i == m - 1 and j == n - 1)
                {
                    res[i][j] = 1;
                }
                else  // free cell
                {
                    long long sum = 0;
                    if (i + 1 < m)  // below
                    {
                        sum += res[i + 1][j];
                    }
                    if (j + 1 < n)  // right
                    {
                        sum += res[i][j + 1];
                    }
                    res[i][j] = sum;
                }
            }
        }

        return res[0][0];
    }
};
